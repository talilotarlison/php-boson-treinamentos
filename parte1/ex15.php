<?php
// Exemplo de declaração e inicialização de uma variável

$nome = "Maria"; // String
$idade = 25; // Inteiro
$altura = 1.75; // Ponto flutuante (float)
$ativo = true; // Booleano

// Exibindo o valor das variáveis
echo "Nome: " . $nome . "<br>";
echo "Idade: " . $idade . " anos<br>";
echo "Altura: " . $altura . " metros<br>";
echo "Ativo: " . ($ativo ? "Sim" : "Não") . "<br>";
?>
