<?php
// Definindo um número hexadecimal
$hex = 0x1A; // 1A é o valor hexadecimal

// Convertendo para decimal
$decimal = hexdec($hex);

echo "Número hexadecimal: " . dechex($hex) . "\n"; // Imprime o número hexadecimal
echo "Número em decimal: " . $decimal . "\n"; // Imprime o valor em decimal
?>
