<?php
for ($contador = 1; $contador <= 5; $contador++) {
    echo "Número: $contador\n";
}

// while: Verifica a condição antes de executar o bloco de código. Se a condição for falsa no início, o código pode não ser executado.

// do...while: Executa o bloco de código pelo menos uma vez, pois a condição é verificada depois.

// for: Usado quando você sabe o número exato de repetições. É mais compacta e controlada por três partes: inicialização, condição e incremento/decremento.
?>
